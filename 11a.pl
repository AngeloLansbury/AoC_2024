$l_10 = log(10);
for (split(/ /, <STDIN>)) {
  $s{$c++} = [$_];
  if ($c > 1) {
    push(@{$s{$c-2}}, $s{$c-1});
  }
}
for $i (0 .. 24) {
  $s = $s{0};
  while (1) {
    if (${$s}[0] == 0) {
      ${$s}[0] = 1;
    } else {
      $l = int(log(${$s}[0]) / $l_10);
      if ($l % 2 == 1) {
        $l = 10 ** (($l + 1) / 2);
        $n = [${$s}[0] % $l];
        push(@{$n}, ${$s}[1]) if $#{$s} > 0;
        ${$s}[0] = int(${$s}[0] / $l);
        ${$s}[1] = $n;
        $s = $n;
        $c++;
      } else {
        ${$s}[0] *= 2024;
      }
    }
    last if $#{$s} == 0;
    $s = ${$s}[1];
  }
}
print $c;
